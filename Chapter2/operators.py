# Arithmatic Operators
a = 10
b = 2
print(a + b) # Addition
print(a - b) # Subtraction
print(a * b) # Multiplication
print(a / b) # Division
print(a % b) # Modulus
print(a ** b) # Exponentiation

# Assignment Operators
x = 5
x += 3  # x = x + 3
print(x)

# comparison Operators
j = 5>2
print(j) # Greater than
k = 5<2
print(k) # Less than
l = 5==2
print(l) # Equal to
m = 5!=2
print(m) # Not equal to

# Logical Operators
# Truth Table of 'and' Operator

print("True and False is:", True and False)
print("True and True is:", True and True)
print("False and False is:", False and False)
print("False and True is:", False and True)

# Truth Table of 'or' Operator
print("True or False is:", True or False) 
print("True or True is:", True or True)
print("False or False is:", False or False)
print("False or True is:", False or True)

# Truth Table of 'not' Operator
print("not True is:", not True) # Logical NOT
print("not False is:", not False) # Logical NOT
print("not (True and False) is:", not (True and False)) # Logical NOT with AND
print("not (True or False) is:", not (True or False)) # Logical NOT with OR

print (not(True)) # Logical NOT
print (not(False)) # Logical NOT
